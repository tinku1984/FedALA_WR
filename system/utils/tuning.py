def run_grid_search(args):
    """
    Perform grid search for tuning buffer_size and ewc_lambda.

    Args:
        args: Command-line arguments or config object.

    Returns:
        Best parameter combination and its performance.
    """

    buffer_sizes = [10, 20, 50, 100]
    ewc_lambdas = [0.05, 0.1]
        
    # buffer_sizes = [5, 10, 20, 50, 100] 
    # ewc_lambdas = [0.05, 0.1, 0.2, 0.3, 0.5]

    # Define parameter ranges for grid search
    # buffer_sizes = [100, 200, 500, 1000, 2000]  # Example values, adjust as needed
    # ewc_lambdas = [0.10, 1, 5, 10, 20]   # Example values, adjust as needed
    # buffer_sizes = [100, 200]  # Example values, adjust as needed
    # ewc_lambdas = [0.10, 1]   # Example values, adjust as needed
    best_params = None
    best_accuracy = -np.inf
    results = []

    print("args : ", args)

    # Iterate through all combinations of buffer_size and ewc_lambda
    for buffer_size, ewc_lambda in itertools.product(buffer_sizes, ewc_lambdas):
        print(f"Testing with buffer_size={buffer_size}, ewc_lambda={ewc_lambda}")
        args.buffer_size = buffer_size
        args.ewc_lambda = ewc_lambda
        args.algorithm = 'FedALA++'
        args.model = 'CNN'
        args.gr = 10
        print("Inside iter +++++++++ ",args)
        global_accuracy = run1(args)

        
        

    #     # Initialize and run FedALA++ server
    #     server = FedALA_PP(args, times=0)  # Adjust 'times' if necessary
    #     server.train()
    #     # Update arguments with current combination
    #     args.buffer_size = buffer_size
    #     args.ewc_lambda = ewc_lambda
    #     # Evaluate the global model
    #     global_accuracy = server.evaluate_global_model()

        # Store the result
        results.append((buffer_size, ewc_lambda, global_accuracy))
        print(f"Result: buffer_size={buffer_size}, ewc_lambda={ewc_lambda}, accuracy={global_accuracy:.4f}")

        # Check if this combination is the best so far
        if global_accuracy > best_accuracy:
            best_accuracy = global_accuracy
            best_params = (buffer_size, ewc_lambda)

    # Print all results
    print("\nGrid Search Results:")
    for res in results:
        print(f"buffer_size={res[0]}, ewc_lambda={res[1]}, accuracy={res[2]:.4f}")

    print(f"\nBest Parameters: buffer_size={best_params[0]}, ewc_lambda={best_params[1]}")
    print(f"Best Accuracy: {best_accuracy:.4f}")

    return best_params, best_accuracy
